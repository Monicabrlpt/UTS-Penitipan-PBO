    private void tblDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDataMouseClicked
        // TODO add your handling code here:
        int baris = tblData.getSelectedRow();
        if (baris != -1) {
            resi.setText(tblData.getValueAt(baris, 0).toString());
            petugas.setText(tblData.getValueAt(baris, 1).toString());
            nopol.setText(tblData.getValueAt(baris, 3).toString());
            SimpleDateFormat date = new SimpleDateFormat ("yyyy-MM-dd");
            Date dateFormatmsk = null;
            Date dateFormatklr = null;
            try {
                dateFormatmsk = date.parse(tblData.getValueAt(baris, 4).toString());
                dateFormatklr = date.parse(tblData.getValueAt(baris, 5).toString());
            } catch (ParseException ex) {
                Logger.getLogger(Penitipan.class.getName()).log(Level.SEVERE, null, ex);
            }
            masuk.setDate(dateFormatmsk);
            keluar.setDate(dateFormatklr);

            if ("Motor".equals(tblData.getValueAt(baris, 2).toString())) {
                mtr.setSelected(true);
            } else {
                mbl.setSelected(true);
            }
        }
    }//GEN-LAST:event_tblDataMouseClicked

