    private void EditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String klr = dateFormat.format(keluar.getDate());
        String msk = dateFormat.format(masuk.getDate());
        if ("".equals(petugas.getText()) ||
            "".equals(resi.getText()) ||
            "".equals(nopol.getText())) {
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error", JOptionPane.WARNING_MESSAGE);
        }else{
            String JK = "";
            if (mtr.isSelected()) {
                JK = "Motor";
            }else{
                JK = "Mobil";
            }
            String SQL = "UPDATE tb_penitipan SET "
            + "NoResi='"+resi.getText()+"',"
            + "NoKendaraan='"+nopol.getText()+"',"
            + "TanggalMasuk='"+msk+"',"
            + "TanggalKeluar='"+klr+"',"
            + "JenisKendaraan='"+JK+"',"
            + "WHERE Petugas='"+petugas.getText()+"'";
            int status = KoneksiDB.execute(SQL);
            if (status == 1) {
                JOptionPane.showMessageDialog(this, "Data berhasil diupdate","Sukses", JOptionPane.INFORMATION_MESSAGE);
                selectData();
            }else{
                JOptionPane.showMessageDialog(this, "Data gagal diupdate", "Gagal", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_EditActionPerformed

